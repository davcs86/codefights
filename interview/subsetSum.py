from collections import Counter
def getSum(ln, n, s, t, mx):
    if s == ln:
        yield t
    else:
        v = t + n[s]
        if v < mx:
            for i in getSum(ln, n, s+1, v, mx):
                yield i
        elif v == mx:
            yield v
        for i in getSum(ln, n, s+1, t, mx):
            yield i


def subsetSum(arr):
    dict = { v: k % 2 for v, k in Counter(arr).iteritems() if k % 2 > 0}
    keys = sorted(list(dict))
    ilen = len(keys)
    if ilen == 0:
        return True
    else:
        # do the maths
        skeys = sum(keys)
        okeys = skeys / 2.0
        pkeys = okeys // 1
        if okeys != pkeys:
            return False
        t = 0
        for i in getSum(ilen, keys, 0, 0, okeys):
            if i == okeys:
                t = i
                break
        return (t == okeys)

